const mongoose = require("mongoose");

const userDetailSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  phoneNo: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  message: {
    type: String,
  },
});

const userDetail = mongoose.model("userDetail", userDetailSchema);
module.exports = userDetail;
